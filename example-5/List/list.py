# This is an example of list and ways to access its elements 

# subjects is a list containing three subjects

subjects = ["dzo","eng","math"]

print(subjects[0])
#output : dzo

print(subjects[1])
#output : eng

print(subjects[2])
# output: math

# The list can also be accessed by loops
for sub in subjects:
  
     print(sub)
    
#Output: dzo, eng, math
