# This example is to check the data types of a variable
# -----------------------------------------------------

name = "Sonam"

age = 16

height = 167.8

passed = True

# type() function is used to check the data type of a variable

print(type(name)) 
# Output <class 'str'> meaning variable, name, stores a string value

print(type(age)) 
# Output <class 'int'>  meaning variable, age, stores a integer value

print(type(height))#
# Output <class ’float’>   meaning variable, height, stores a float value

print(type(passed))
#Ouput <class ‘bool'> meaning variable, passed, stores a boolean value

